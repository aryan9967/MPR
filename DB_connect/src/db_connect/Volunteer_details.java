    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(v_name.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            v_name.requestFocus();
        }
        else if(v_age.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "All fields are required");
            v_age.requestFocus();
        }
        else if(v_contact.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "All fields are required");
            v_contact.requestFocus();
        }
        else if(v_id.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "All fields are required");
            v_id.requestFocus();
        }
        else if(v_address.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "All fields are required");
            v_address.requestFocus();
        }
        else{
            try {
                pst=con.prepareStatement("insert into volunteer_details values(?,?,?,?,?)");
                pst.setString(1, v_name.getText());
                pst.setString(2, v_age.getText());
                pst.setString(3, v_contact.getText());
                pst.setString(4, v_id.getText());
                pst.setString(5, v_address.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Volunteer Added successfully");
                ShowRecordInTable();
                v_name.setText("");
                v_age.setText("");
                v_contact.setText("");
                v_id.setText("");
                v_address.setText("");

            } catch (SQLException ex) {
                Logger.getLogger(Donation_details.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed


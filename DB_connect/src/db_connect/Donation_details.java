    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(food_item.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            food_item.requestFocus();
        }
        else if(qauntity.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "All fields are required");
            qauntity.requestFocus();
        }
        else if(donee_name.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "All fields are required");
            donee_name.requestFocus();
        }
        else if(donee_contact.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "All fields are required");
            donee_contact.requestFocus();
        }
        else if(donee_address.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "All fields are required");
            donee_address.requestFocus();
        }
        else{
            try {
                pst=con.prepareStatement("insert into donation_details values(?,?,?,?,?)");
                pst.setString(1, food_item.getText());
                pst.setString(2, qauntity.getText());
                pst.setString(3, donee_name.getText());
                pst.setString(4, donee_contact.getText());
                pst.setString(5, donee_address.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Donation Added successfully");
                ShowRecordInTable();
                food_item.setText("");
                qauntity.setText("");
                donee_name.setText("");
                donee_contact.setText("");
                donee_address.setText("");
                
            } catch (SQLException ex) {
                Logger.getLogger(Donation_details.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed


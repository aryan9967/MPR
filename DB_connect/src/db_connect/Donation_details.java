    public void ShowRecordInTable(){
        int c=0;
        try {
            pst=con.prepareStatement("Select * from donation_details");
            rs=pst.executeQuery();
            ResultSetMetaData rsmd= rs.getMetaData();
            c=rsmd.getColumnCount();
            DefaultTableModel DModel=(DefaultTableModel) jTable1.getModel();
            DModel.setRowCount(0);
            while(rs.next()){
                Vector column=new Vector();
                for(int i=1; i<=c; i++){
                    column.add(rs.getString("Food item"));
                    column.add(rs.getString("Qauntity"));
                    column.add(rs.getString("Donee_name"));
                    column.add(rs.getString("Donee_contact"));
                    column.add(rs.getString("Donee_address"));
                }
                DModel.addRow(column);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Donation_details.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
